num1 = float(input("Digite um número: "))
num2 = float(input("Digite outro número: "))

if num1 % 2 == 0:
    print(f"O número {num1} é multiplo de 2.")
else:
    print(f"O número {num1} não é multiplo de 2.")

if num2 % 2 == 0:
    print(f"O número {num2} é multiplo de 2.")
else:
    print(f"O número {num2} não é multiplo de 2.")